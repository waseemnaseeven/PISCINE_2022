char	*ft_findone(char *dict, char str)
{
	int	i;

	i = 0;
	if (str == '\0')
		return (0);
	if (str == '1')
		return (0);
	while (dict[i] != '\0')
	{	
		if (dict[i] == str && dict[i + 1] != '0')
			return (&dict[i]);
		i++;
	}
	return (0);
}
