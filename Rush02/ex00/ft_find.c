char	*ft_find(char *dict, char *str)
{
	int	i;
	int	j;

	i = 0;
	if (str[0] == '\0')
		return (str);
	while (dict[i] != '\0')
	{
		j = 0;
		while ((dict[i + j] != '\0') && (dict[i + j] == str[j]))
		{
			if (str[j + 1] == '\0')
			{
				if ((dict[i + j + 1] == ' ') || dict[i + j + 1] == ':')
					return (&dict[i]);
			}
			j++;
		}
		i++;
	}
	return (0);
}
